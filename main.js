const imgCont = document.querySelector('.img-cont');
function renderImages(images) {
    imgCont.innerHTML = '';

    images.forEach(img => {
        const imgItem = document.createElement('div');

        imgItem.className = 'img-item';

        imgItem.innerHTML = `
            <div class="img">
                <img src="${img.image}" alt="">
            </div>
            <div class="lay-cont">
                 <a class="download" href="${img.image}" download="${img.image}"><i class="fa-solid fa-download"></i> Download</a>
            </div>
        `;
        imgItem.addEventListener('click', ()=>{
            displayFull(img.id)
        })
        imgCont.appendChild(imgItem);
    });
}
