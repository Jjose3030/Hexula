const full = document.querySelector('.full');
const modal = document.querySelector('.modal')


function activeState(item, value){
    item.classList.add(value)
}
function closeState(item, value){
    item.classList.remove(value)
}


//for filtering images based on categories

const filter = document.querySelector('#filter');

filter.addEventListener('change', (e) => {

    const value = e.target.value;

    if (value === 'all') {
        renderImages(imageGallery);
        return;
    }

    const filteredImages = imageGallery.filter(img =>
        img.category === value
    );

    renderImages(filteredImages);
});


//for displaying the full size of the image 

let currentIndex = 0;

function displayFull(imgId){

    currentIndex = imageGallery.findIndex(
        img => img.id === imgId
    );

    showImage();
    activeState(modal, 'active')
}

function showImage(){

    const vFull = document.querySelector('.v-full');

    vFull.innerHTML = '';

    const image = imageGallery[currentIndex];

    const vFullCont = document.createElement('div');

    vFullCont.classList.add('v-full-cont');

    vFullCont.innerHTML = `
        <button class="close"><i class="fa-solid fa-xmark"></i></button>
        
        <img src="${image.image}" alt="">
        
    `;

    vFull.appendChild(vFullCont);

    activeState(vFull, 'active');

    document.querySelector('.close')
    .addEventListener('click', ()=>{
        closeState(vFull,'active');
        closeState(modal,'active');
    });

    

}

//keyboard events for navigating to the next or previous image using the right and left arrow keys on the keyboard
document.addEventListener('keydown', (e)=>{

    const vFull = document.querySelector('.v-full');

    if(!vFull.classList.contains('active')) return;

    if(e.key === 'ArrowRight'){
        currentIndex++;
       nextImage()
    }

    if(e.key === 'ArrowLeft'){
        currentIndex--;

        prevImage()
    }

    if(e.key === 'Escape'){
        closeState(vFull,'active');
    }

});



function nextImage() {
    if (currentIndex >= imageGallery.length) {
        currentIndex = 0;
    }

    showImage();
}
function prevImage() {
    if (currentIndex < 0) {
        currentIndex = imageGallery.length - 1;
    }

    showImage();
}

document.querySelector('.next-btn').addEventListener('click', ()=>{
        currentIndex++;
        nextImage()
    })
    document.querySelector('.prev-btn').addEventListener('click', ()=>{
        currentIndex--;
        prevImage()
    })

